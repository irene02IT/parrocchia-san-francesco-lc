                  <p>
In quel tempo. Il Signore Gesù ritornò in Galilea con la potenza dello Spirito e la
sua fama si diffuse in tutta la regione. Insegnava nelle loro sinagoghe e gli
rendevano lode. Venne a Nàzaret, dove era cresciuto, e secondo il suo solito, di
sabato, entrò nella sinagoga e si alzò a leggere. Tutti gli davano testimonianza ed
erano meravigliati delle parole di grazia che uscivano dalla sua bocca e dicevano:
«Non è costui il figlio di Giuseppe?». Ma egli rispose loro: «Certamente voi mi
citerete questo proverbio: “Medico, cura te stesso. Quanto abbiamo udito che
accadde a Cafàrnao, fallo anche qui, nella tua patria!”». Poi aggiunse: «In verità io
vi dico: nessun profeta è bene accetto nella sua patria».
                  </p>
