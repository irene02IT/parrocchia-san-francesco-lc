                <p>
<p class="font-italic">“Tutti gli davano testimonianza ed erano meravigliati delle parole di grazia che
uscivano dalla sua bocca”.</p> Quando il Signore parla non lo fa in un modo qualsiasi
ma usa parole che toccano direttamente il cuore di chi ascolta. La meraviglia che
sfocia da tutti coloro che si mettono in ascolto è dovuta proprio a questo. La parola
che Gesù dice ad ogni cuore è proprio quella che quella persona ha bisogno di sentirsi
dire e questo mette in difficoltà perché ognuno di noi ha una reazione istintiva
quando si sente toccato nel profondo: si lascia interpellare e accoglie quella parola
oppure si chiude e rifiuta di mettersi in discussione. Quello che avviene a Nazaret è
proprio questo! Tutti rimangono meravigliati, vengono interpellati direttamente dalle
parole che Gesù dice. Eppure la loro “conoscenza” di lui (pensano di sapere chi sia)
impedisce loro di aderire a ciò che li ha stupiti e coinvolti.
Quante volte capita anche a noi di pensare che una relazione vissuta nell’amore
non sia possibile perché pensiamo: “sarebbe troppo bello se fosse così”. Il Signore
oggi ci mette in guardia da questo pericolo!
<p class="font-italic">“In verità io vi dico: nessun profeta è bene accetto nella sua patria”.</p> Nella sua
patria tutti pensano di conoscere un profeta, di sapere i suoi limiti, di aver visto i suoi
difetti, di averlo “inquadrato”. Non gli permettono di essere di più. Quante volte
capita anche a me, a noi, di pensare di conoscere una persona e dire: “Non farà mai
nulla di buono, di meglio di quello che fa! Non cambierà mai!”. Ma lo Spirito è sopra
di lui “con la potenza dello Spirito” come lo era su Gesù. La nostra fede in Dio si
basa proprio sulla speranza che Dio possa intervenire nella vita di un uomo e
migliorarla fino alla realizzazione. La bellezza della nostra vita in Cristo è proprio in
questa adesione alla parola che Dio manda in ogni persona che ci circonda.
Altrimenti saremmo anche noi come i nazareni che pensando di conoscere una
persona non permettono a Dio di entrare in relazione con loro.<br><br>
<p class="font-italic">Buona giornata</p>
